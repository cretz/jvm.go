package instructions

import (
	"github.com/cretz/jvm.go/jvmgo/jvm/rtda"
)

// Shift left int
type ishl struct{ NoOperandsInstruction }

func (self *ishl) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopInt()
	v1 := stack.PopInt()
	s := uint32(v2) & 0x1f
	result := v1 << s
	stack.PushInt(result)
}

